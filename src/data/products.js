export function normalizeProducts(customItems = []) {
  return baseProducts.map((category) => {
    const baseItems = category.items.map(([id, subCategory, name, detail]) => ({
      id,
      subCategory,
      name,
      detail,
      categoryId: category.id,
      categoryName: category.name,
      isCustom: false
    }));

    const extraItems = customItems
      .filter((item) => item.categoryId === category.id)
      .map((item) => ({
        ...item,
        categoryName: category.name,
        isCustom: true
      }));

    return {
      ...category,
      items: [...baseItems, ...extraItems]
    };
  });
}
